                            <li class="subList__item">Biết định hướng và phát triển nghề nghiệp phù hợp với đam mê và
                                tài năng.</li>
                            <li class="subList__item">Phát triển toàn diện, từ kiến thức, kĩ năng đến phản xạ nhanh nhạy
